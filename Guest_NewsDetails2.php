          <p><b>Malaysian firm Axis Real Estate Investment Trust (Axis-REIT)</b> has sold a former steel complex in Johor to a data center operator.
                The company this week announced it has entered into a sale and purchase agreement with an unnamed data center operator for the disposal of Axis Steel Centre@SiLC.</p>
          <p>The deal is valued at RM162 million ($33.9m) and is set to close in the second half of the year.
             The 10-hectare property – described by Axis-REIT as a steel fabrication facility – is located along Jalan SiLC 1/5, within the locality known as Kawasan Perindustrian SiLC in Nusajaya, Johor.</p>
           <p> “The site features two single-story detached factories, a two-story office building, a two-story canteen and maintenance office, a single-story training center, a three-story worker hostel, and other ancillary buildings.</p>
           <p>"The manager believes that in the nine years of owning this property, it has maximized the income-generating potential of this asset and there is limited upside for future rental growth in its current capacity as a manufacturing facility,” Axis-REIT said.</p>
           <p>“Given the favorable price of this asset sale and leveraging on the best and highest use of the property as a data center development, this is an opportune time to dispose of the property and to redeploy the capital towards potential yield-accretive properties.”</p> 
           <p>The buyer wasn’t named, but the terms of the sale include the unnamed data center gaining an agreement with local energy firm Tenaga Nasional by the end of August 2024 for the supply of energy to a planned data center on the site.</p>
           <p>Johor, located in Malaysia’s south, is just across the border from Singapore. Though Singapore is starting to ease restrictions around its ongoing moratorium on new data center projects, Johor has grown a sizeable data center market of its own as developments overspill from the city-state.</p>
